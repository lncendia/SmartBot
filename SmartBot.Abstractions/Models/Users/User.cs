/// <summary>
/// Модель пользователя системы отчетности
/// </summary>
/// <remarks>
/// Основная сущность, содержащая информацию о пользователе и его состоянии в системе.
/// Хранит как персональные данные, так и рабочую контекстную информацию.
/// </remarks>
public class User
