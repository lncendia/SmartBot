namespace SmartBot.Abstractions.Configuration;

/// <summary>
/// Конфигурация анализа отчетов сотрудников
/// </summary>
/// <remarks>
/// Содержит настройки для системы автоматического анализа текстовых отчетов:
/// - Включение/отключение анализа
/// - Критерии оценки качества отчетов
/// Используется для проверки полноты и качества отчетов сотрудников
/// </remarks>
public class ReportAnalysisConfiguration
{
    /// <summary>
    /// Флаг, указывающий включен ли автоматический анализ отчетов
    /// </summary>
    /// <value>
    /// true - анализ активен, отчеты проверяются автоматически
    /// false - анализ отключен
    /// </value>
    public required bool Enabled { get; init; }
    
    /// <summary>
    /// Минимальный проходной балл для отчета
    /// </summary>
    /// <value>
    /// Целое число от 0 до 10. Отчеты с оценкой ниже этого значения
    /// будут помечаться как требующие доработки.
    /// </value>
    public required int MinScore { get; init; }
}